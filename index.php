            const parentParagraph = $(this).closest('.card-text');
            const fullText = parentParagraph.data('full-text');
            
            if (parentParagraph.hasClass('expanded')) {
                // Collapse text
                parentParagraph.text(fullText.split(' ').slice(0, 15).join(' ') + '... ');
                $(this).text('Read more');
                parentParagraph.removeClass('expanded');
            } else {
                // Expand text
                parentParagraph.text(fullText);
                $(this).text('Read less');
                parentParagraph.addClass('expanded');
            }
        });
});
</script>








