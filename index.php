<script>
    document.addEventListener('DOMContentLoaded', function () {
        const steps = Array.from(document.querySelectorAll('.step'));
        let currentStep = 0;

        const updateStepIndicator = () => {
            document.getElementById('stepIndicator').innerText = `STEP ${currentStep + 1} OF 3`;
        };

        const showStep = (stepIndex) => {
            steps.forEach((step, index) => {
                step.classList.toggle('hidden', index !== stepIndex);
            });
            updateStepIndicator();
        };

        document.getElementById('next-1').addEventListener('click', function () {
            if (document.getElementById('name').value.trim()) {
                currentStep = 1;
                showStep(currentStep);
            } else {
                alert('Please enter your name.');
            }
        });

        document.getElementById('next-2').addEventListener('click', function () {
            if (document.getElementById('phone').value.trim()) {
                currentStep = 2;
                showStep(currentStep);
            } else {
                alert('Please enter your phone number.');
            }
        });

        document.getElementById('prev-2').addEventListener('click', function () {
            currentStep = 0;
            showStep(currentStep);
        });

        document.getElementById('prev-3').addEventListener('click', function () {
            currentStep = 1;
            showStep(currentStep);
        });

        // Handle form submission via AJAX
        document.getElementById('multiStepForm').addEventListener('submit', function (event) {
            event.preventDefault();
            
            const formData = new FormData(this);

            fetch(this.action, {
                method: this.method,
                body: formData,
            })
            .then(response => response.json())
            .then(data => {
                if (data.success) {
                    alert(data.message);
                    window.location.href = './';  // Redirect to index page
                } else {
                    alert(data.message);
                }
            })
            .catch(error => {
                console.error('Error:', error);
                alert('There was an error submitting the form. Please try again.');
            });
        });

        showStep(currentStep);
    });
</script>
